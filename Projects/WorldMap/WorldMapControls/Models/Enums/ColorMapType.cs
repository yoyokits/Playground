namespace WorldMapControls.Models.Enums
{
    /// <summary>
    /// Defines well-known colormap types similar to MATLAB colormaps.
    /// </summary>
    public enum ColorMapType
    {
        /// <summary>
        /// Blue to red colormap (MATLAB jet).
        /// </summary>
        Jet,

        /// <summary>
        /// Hue-Saturation-Value colormap.
        /// </summary>
        HSV,

        /// <summary>
        /// Red to yellow colormap.
        /// </summary>
        Autumn,

        /// <summary>
        /// Blue to white colormap.
        /// </summary>
        Winter,

        /// <summary>
        /// Green to yellow colormap.
        /// </summary>
        Spring,

        /// <summary>
        /// Green to magenta colormap.
        /// </summary>
        Summer,

        /// <summary>
        /// Black to white colormap.
        /// </summary>
        Gray,

        /// <summary>
        /// Black to red colormap.
        /// </summary>
        Hot,

        /// <summary>
        /// Dark blue to light blue colormap.
        /// </summary>
        Cool,

        /// <summary>
        /// Blue to cyan to yellow to red colormap.
        /// </summary>
        Viridis,

        /// <summary>
        /// Dark purple to bright yellow colormap.
        /// </summary>
        Plasma,

        /// <summary>
        /// Dark blue to bright yellow colormap.
        /// </summary>
        Inferno,

        /// <summary>
        /// Black to magenta to white colormap.
        /// </summary>
        Magma,

        /// <summary>
        /// White to blue colormap.
        /// </summary>
        Blues,

        /// <summary>
        /// White to red colormap.
        /// </summary>
        Reds,

        /// <summary>
        /// White to green colormap.
        /// </summary>
        Greens,

        /// <summary>
        /// White to orange colormap.
        /// </summary>
        Oranges,

        /// <summary>
        /// White to purple colormap.
        /// </summary>
        Purples,

        /// <summary>
        /// Blue to white to red diverging colormap.
        /// </summary>
        RdBu,

        /// <summary>
        /// Red to white to blue diverging colormap.
        /// </summary>
        Spectral,

        /// <summary>
        /// Rainbow colormap.
        /// </summary>
        Rainbow
    }
}